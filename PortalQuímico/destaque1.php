            <p class="flow-text center-align grey-text text-darken-3">
                O Desenho Universal para a Aprendizagem (DUA) é uma abordagem pedagógica inovadora que visa tornar o ensino mais acessível e inclusivo desde o planejamento, inspirada no conceito de desenho universal da arquitetura, que cria espaços utilizáveis por todos sem adaptações posteriores.
                <br>Desenvolvido pelo Center for Applied Special Technology (CAST) nos Estados Unidos, o DUA baseia-se em três princípios fundamentais: oferecer múltiplos meios de representação (para apresentar o conteúdo de formas variadas, como através da escrita, de forma oral ou de projetos práticos); múltiplos meios de ação e expressão (permitindo que os alunos demonstrem conhecimento de maneiras diversas, como escrita, fala ou projetos práticos); e múltiplos meios de engajamento (motivando os alunos com opções que atendam a interesses e níveis de desafio diferentes).
                <br>Essa estratégia é particularmente importante na educação, especialmente em disciplinas como a Química, onde conceitos abstratos podem ser barreiras para alunos com necessidades específicas ou estilos de aprendizado variados. Ao aplicar o DUA, o professor planeja aulas flexíveis que reduzem a necessidade de adaptações individuais, promovendo a inclusão escolar e garantindo que todos os alunos tenham oportunidades iguais de participação e sucesso.
                <br>Estudos, como os de Zerbato e Mendes (2018), destacam que o DUA facilita a permanência e o progresso de estudantes com necessidades específicas, enquanto Bettio, Miranda e Schmidt (2021) enfatizam sua aplicação na educação infantil para práticas inclusivas desde cedo.
